# List
# mutable
# accessing the element by index is allowed
Arr = [10,20,30,40,10]
print(Arr)
print(type(Arr))

# Modifying the element in the list is allowed
Arr[3]= 23
print(Arr)

# tuple
# immutable
# accessing the element by index is allowed

# Modifying the elements in the tuple is not allowed

Brr = (10,20,30,40,10)
print(Brr)
print(type(Brr))

